# Criação de um array com 15 números inteiros aleatórios
numeros = [42, 23, 16, 15, 8, 4, 89, 7, 90, 3, 66, 33, 57, 28, 10]

numeros.sort()
print("Array ordenado de forma crescente:", numeros)

numeros.sort(reverse=True)
print("Array ordenado de forma decrescente:", numeros)

# Criação de um array de strings
dados_pessoais = ["nome", "dataNascimento", "cpf", "rg"]

dados_pessoais.sort()
print("Array de strings ordenado de forma crescente:", dados_pessoais)

dados_pessoais.sort(reverse=True)
print("Array de strings ordenado de forma decrescente:", dados_pessoais)

